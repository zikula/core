namespace Zikula\Bundle\HookBundle\Hook;

/**
 * Content filter hook.
 */
class FilterHook extends Hook
{
    private $data;

    public function __construct($data = null)
    {
        $this->setData($data);
    }

    public function getData()
    {
        return $this->data;
    }

    public function setData($data): void
    {
        $this->data = $data;
    }
}
